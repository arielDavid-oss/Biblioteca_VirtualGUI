    private void btnDevolverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDevolverActionPerformed
  String titulo = txtTitulo.getText();
    try {
        Libro libro = readLibro(titulo);

        if (libro != null) {
            if (!libro.isPrestado()) {
                JOptionPane.showMessageDialog(this, "El libro no está prestado");
                return;
            }

            txtFechap.setText(libro.getF_Prestamo());
            txtNombre.setText(libro.getNombrePrestamo());

            libro.setF_Prestamo("");
            libro.setNombrePrestamo("");
            libro.setPrestado(false);
            libro.setLeido(true);

            writeLibro(titulo, libro);
            JOptionPane.showMessageDialog(this, "Libro devuelto correctamente");
        } else {
            JOptionPane.showMessageDialog(this, "El libro no está en el archivo");
        }
    } catch (IOException ex) {
        JOptionPane.showMessageDialog(this, "Error al acceder al archivo.", "Error", JOptionPane.ERROR_MESSAGE);
        ex.printStackTrace();
    }
    }//GEN-LAST:event_btnDevolverActionPerformed


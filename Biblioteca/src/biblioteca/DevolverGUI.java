 private Libro readLibro(String titulo) throws IOException {
    raf.seek(0); // Iniciar desde el principio del archivo
    while (raf.getFilePointer() < raf.length()) {
        byte[] record = new byte[Libro.SIZE];
        raf.read(record);
        Libro libro = Libro.fromBytes(record);
        if (libro.getTitulo().equalsIgnoreCase(titulo)) {
            return libro;
        }
    }
    return null; // Si el libro no se encuentra en el archivo
}

private void writeLibro(String titulo, Libro libro) throws IOException {
    raf.seek(0); // Iniciar desde el principio del archivo
    while (raf.getFilePointer() < raf.length()) {
        byte[] record = new byte[Libro.SIZE];
        raf.read(record);
        Libro existingLibro = Libro.fromBytes(record);
        if (existingLibro.getTitulo().equalsIgnoreCase(titulo)) {
            raf.seek(raf.getFilePointer() - Libro.SIZE);
            raf.write(libro.toBytes());
            return;
        }
    }
}
